void SetOIDType(BYTE* buffer, int bufferLen, uint32_t& oidType)
{
	oidType = 0;
	if (*(uint8_t*)(buffer + bufferLen - 1) == 0xFF)
		oidType = 1;
	else if (*(uint32_t*)(buffer + 12) == 0)
	{
		oidType = 2;
	}
}

